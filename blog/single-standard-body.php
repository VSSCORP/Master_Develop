        <p class="lead drop-cap">I’m quiet, but I know more than I say and think more than I speak and observe more than you know me. Nothing is lost, everything remains deep inside the heart. Remembering the people you love and admire is like counting the stars.
            Out of millions of constellations we pick the brightest.If I have to say about the one I met, who is always the intense, raw drummer of a war song who keeps me alive for every war I face.</p>

        <p>It will be a whole new world with such people and every beginning starts with them. You can see things from different perspectives, roses might be pretty and red but I feel like the PETALS of thoughts are threaded together by a
            bond of love on the stem. The people we adore are archives of thoughts like roses and I love them for that. When we chose a person to live with, they shouldn’t be chosen by a pretty face but by pretty mind.There are always
            people in our heart that we care about but it’s just we don’t realize how much until they’re gone. I suppose to say if you’ve to choose between the right person and the kind person choose the kind.
        </p>

        <p>
            <img src="images/wheel-1000.jpg" srcset="images/wheel-2000.jpg 2000w, images/wheel-1000.jpg 1000w, images/wheel-500.jpg 500w" sizes="(max-width: 2000px) 100vw, 2000px" alt="">
        </p>

        <h2>Perspective</h2>

        <p>In my mind, I always have a person with no appearance and emotions but just in form of thoughts waiting for someone who would understand my introvert mind, it’s always been within me like a pearl in the oyster inspiring me and
            making me walk through. Oh dear, with the hues of this nature and odors of rhymes and songs you let my heart sway like a pigeon in the colors of your embrace.</p>

        <blockquote>
            <p>Worrying about the things that are not in hands is suffering twice.</p>
        </blockquote>

        <p>Either I afloat or drown but for sure I reach the shore. A question always pesters my mind always is whether I should embrace this world or make the comfort in my own world. My world is filled with a lot of moments which have not
            happened momentarily. I’m in dark now but not hiding in shadows but there comes a day I would shine and when I do I would shine the brightest.</br>
            We all worry about the way things happen but worrying means suffering twice. The worse has already happened, we have to let it go, because living is ain’t dying, it takes a lot to go through to live than to die in a moment. We’re always left with two
            choices, the easy one and the right one. The easy one lives and the right one lives on.</p>

        <h3>Constraints</h3>

        <p>When a love was built and not shared to many constraining it to the narrow limits then it would collapse like the shore-sand castle leaving the heart wounded. The wounds, they leave cracks but if you plant the seed of courage inside
            your heart it grows, and it grows blooms like the brightest night light and illuminate through the cracks and letting the world know you ought to make something beautiful. Never hide your wounds and the cracks it made there
            comes someone who fills the cracks with love.
            <p>The silence makes the loudest screams. My conscience says one day someone would come into my life but when they come I wish to embrace their flaws. You should never want people, you should own them, The best people always comes
                with time. Like million cells born every day build me, the thousands of words I never said forms into sentences to define me that someone would read.</p>

            <h3>Pretend</h3>

            <p>It’s a lot more difficult to pretend that you do have feelings when you don’t than to pretend you don’t have feelings when you do. It’s very often we think too much about a thing or a person but we talk very less about them.
                The people we earn and own they should be with us when you’re successful because when you succeed whatever the things you say even the dumbest things the people around you will always be nice to you either they don’t get
                understand or they don’t want to express. The very first rule of people who want to destroy you is that they never warn you when you are making a mistake. But the people who foresaw your success right from the start would
                tell you and remind you when you’re losing the path, they never change nor find a reason to. Success is always luck, but failure is gifted because the failure leaves you only when you truly understand her.</p>

